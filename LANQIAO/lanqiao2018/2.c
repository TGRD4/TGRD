#include<stdio.h>
int main() {
    int sum1=0;
    int sum2=0;
    int number=0;
    for(int i=0;i<10;i++) {
        for(int j=0;j<10;j++) {
            scanf("%d",&number);
            while(number) {
                if(number%2==0) {           //2是一个因数
                    number/=2;
                    sum1++;
                }
                else if(number%5==0) {      //5是一个因数
                    number/=5;
                    sum2++;
                }
                else {                      //因为要求末尾是连续的0，所以只要2/5不是连续出现就不计入最长位数
                    break;
                }
            }
        }
    }
    printf("%d\n",sum1>sum2?sum2:sum1);     //取两个数中较小的数
    return 0;
}
