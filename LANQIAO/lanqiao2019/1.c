#include<stdio.h>
int f[20190324];                            //long long 也不行，必须数组（数组空间太大的时候要放在main()外面）
int main() {
    f[0]=f[1]=f[2]=1;
    for(int i=3;i<20190324;i++) {           //第20190324项下标是20190323
        f[i]=(f[i-1]+f[i-2]+f[i-3])%10000;  //数值过大会报错且只要求最后四位
    }
    printf("%d\n",f[20190323]);             //输出的也是第20190324项而不是下标为20190324的项数
    return 0;
}
